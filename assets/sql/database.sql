CREATE TABLE KhachHang
(
	MaKH VARCHAR(10) NOT NULL,
	TenKH NVARCHAR(255) NOT NULL,
	SDT CHAR(10),
	DiaChi NVARCHAR(MAX),
	Email VARCHAR(255),
	CONSTRAINT PK_KhachHang PRIMARY KEY(MaKH),
